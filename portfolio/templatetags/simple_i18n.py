@register.simple_tag(takes_context=True)
def trans(context, message):
    """
    Simple translation tag that looks up translations based on current language.
    Usage: {% trans "Dashboard" %}
    """
    request = context.get('request')

    # Try to get language from session, URL, or browser preference
    language = 'en'  # default

    if request:
        # Check if language is set in URL path
        path = request.path
        if path.startswith('/es/'):
            language = 'es'
        elif hasattr(request, 'session') and 'django_language' in request.session:
            language = request.session['django_language']
        elif hasattr(request, 'LANGUAGE_CODE'):
            language = getattr(request, 'LANGUAGE_CODE', 'en')

    # Get translation or fallback to original message
    translations = TRANSLATIONS.get(language, TRANSLATIONS['en'])
    translated = translations.get(message, message)

    return mark_safe(translated)


@register.simple_tag(takes_context=True)
def get_current_language(context):
    """Get the current language code."""
    request = context.get('request')

    if request:
        path = request.path
        if path.startswith('/es/'):
            return 'es'
        elif hasattr(request, 'session') and 'django_language' in request.session:
            return request.session['django_language']

    return 'en'


